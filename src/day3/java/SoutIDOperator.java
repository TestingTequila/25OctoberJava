package day3.java;

public class SoutIDOperator
{
    public static void main(String[] args) {

        int num =10;
        System.out.println(++num);

        char c = 'a';//97
        System.out.println(c++);//a

        char d = 'a';
        System.out.println(++d);//b

        char e = 'a';
        System.out.println((int)++e);//98
    }
}
